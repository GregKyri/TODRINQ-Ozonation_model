# Ensure naming consistency with your training (mapping our internal names to your specific model names)

def run_model_prediction(model_key, input_df):
    """Helper to scale, predict, and unscale for a specific model."""
    m_info = MODELS[model_key]
    scaler = m_info['f_scaler']

    # 1. Align with Scaler
    scaler_features = list(scaler.feature_names_in_)
    X_full = pd.DataFrame(0, index=input_df.index, columns=scaler_features)
    for col in input_df.columns:
        if col in X_full.columns:
            X_full[col] = input_df[col]

    # 2. Scale and subset features
    X_scaled = scaler.transform(X_full)
    model_feat_indices = [scaler_features.index(c) for c in m_info['features']]
    X_model = X_scaled[:, model_feat_indices]

    # 3. Predict and Inverse Scale
    y_scaled = m_info['model'].predict(X_model)
    y_unscaled = m_info['t_scaler'].inverse_transform(y_scaled.reshape(-1, 1)).ravel()
    return y_unscaled[0]

